void print_vector(Vector v) {
    for (int i = 0; i < v.size(); ++i) {
        cout << v[i] << " ";
    }
    cout << "\n";
}

void print_matrix(Basis mat){
    for (const auto& row : mat) {
        for (auto elem : row) {
            cout << elem << ' ';
        }
        cout << '\n';
    }
}

